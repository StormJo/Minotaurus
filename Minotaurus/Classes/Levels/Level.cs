            {
                Map = Level1;
            }
            else
            {
                Map = Level2;
            }

            //Initializing Lists
            gameObjects = new List<IGameObject>();
            Entities = new List<IPlayer>();

            //Adding Entities
            heroMino = new Hero(new Vector2(0, 0));
            Entities.Add(heroMino);

            //Loading Textures + Adding GameObjects
            gameObjects.Add(new Slime(Game1.Textures["slime_jump"]));
            gameObjects.Add(new GhostEnemy(heroMino, Game1.Textures["GhostSprite"]));
            LoadLevel(Game1.Textures["tileset"]);
            LoadLevel(Game1.Textures["props"]);
            LoadLevel(Game1.Textures["Coin"]);
            LoadLevel(Game1.Textures["HeartIcon"]);

        }

        public List<IGameObject> GetGameObjects()
        {
            return gameObjects;
        }

        public void Update(GameTime gameTime)
        {
            foreach (var gameObject in gameObjects) 
            {
                gameObject.Update(gameTime);
            }
            foreach (var entity in Entities)
            {
                entity.Update(gameTime);
            }
        }
        public void Draw(SpriteBatch spriteBatch)
        {

            if (heroMino.healthManager.isDead)
            {
                Game1.ChangeState(new GameOverState());
            }
            else
            {
                //Drawing All gameObjects
                foreach (var gameObject in gameObjects)
                {
                    gameObject.Draw(spriteBatch);
                }
                //Drawing All Entities
                foreach (var entity in Entities)
                {
                    entity.Draw(spriteBatch);
                }
                //Drawing HP
                for (int i = 1; i < heroMino.healthManager.CurrentHealth + 1; i++)
                {
                    spriteBatch.Draw(Game1.Textures["HeartIcon"], new Vector2(10 + (i-1) * 45,10), new Rectangle(0,0, 90, 90), Color.White, 0f, Vector2.Zero, .5f, SpriteEffects.None, 0f);
                }
                //Drawing Points
                spriteBatch.Draw(Game1.Textures["Coin"], new Vector2(10, 50), new Rectangle(72, 72, 215, 215), Color.White, 0f, Vector2.Zero, .2f, SpriteEffects.None, 0f);
                spriteBatch.DrawString(Game1.Arial, heroMino.pointManager.Points.ToString(), new Vector2(70, 50), Color.Black, 0, Vector2.Zero, 2f, SpriteEffects.None, 0);
            }
        }


        public void LoadLevel(Texture2D texture)
        {
            for (int i = 0; i < Map.GetLength(0); i++)
            {
                for (int j = 0; j < Map.GetLength(1); j++)
                {
                    if (texture.Name == "tileset")
                    {
                        if (Map[i, j] > 0 && Map[i, j] <= 3)
                        {
                            gameObjects.Add(new GrassTile(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                        if (Map[i, j] > 3 && Map[i, j] <= 6)
                        {
                            gameObjects.Add(new GroundTile(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                        if (Map[i, j] > 6 && Map[i, j] <= 9)
                        {
                            gameObjects.Add(new FloatingIslandGrass(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                        if (Map[i, j] > 9 && Map[i, j] <= 13)
                        {
                            gameObjects.Add(new TransitionGroundTile(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                        if (Map[i, j] >= 14 && Map[i, j] <= 21)
                        {
                            gameObjects.Add(new EnvirnmontProp(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                    }
                    if (texture.Name == "props")
                    {
                        if (Map[i, j] >= 22 && Map[i, j] <= 22)
                        {
                            gameObjects.Add(new NoCollidingProp(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                        if (Map[i, j] >= 23 && Map[i, j] <= 23)
                        {
                            gameObjects.Add(new DamgeAbleProp(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                    }
                    if(texture.Name == "Coin")
                    {
                        if (Map[i, j] >= 24 && Map[i, j] <= 24)
                        {
                            gameObjects.Add(new Coin(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                    }
                    if (texture.Name == "HeartIcon")
                    {
                        if (Map[i, j] >= 25 && Map[i, j] <= 25)
                        {
                            gameObjects.Add(new Heart(j * tileSize, i * tileSize, texture, Map[i, j]));
                        }
                    }
                }
            }


        }
